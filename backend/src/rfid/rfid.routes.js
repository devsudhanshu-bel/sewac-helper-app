const express = require("express");

const router = express.Router();

const {

  createRFID,

  mapRFID,

  getAllRFIDMappings,

  getUnmappedRFIDs,

  getRFIDByValue,

  getCitizenRFIDs,

} = require("./rfid.controller");





/*
|--------------------------------------------------------------------------
| Create RFID
|--------------------------------------------------------------------------
| POST /api/v1/rfid/create/:rfid
| GET  /api/v1/rfid/create/:rfid
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/create/A7F9C2D8E4B1X6Q3M5N8P2R7T1Y4U9W6K3L8V2Z53060
|--------------------------------------------------------------------------
*/
router.post(
  "/create/:rfid",
  createRFID
);

router.get(
  "/create/:rfid",
  createRFID
);





/*
|--------------------------------------------------------------------------
| Get All RFID Mappings
|--------------------------------------------------------------------------
| GET /api/v1/rfid/all
|--------------------------------------------------------------------------
*/
router.get(
  "/all",
  getAllRFIDMappings
);





/*
|--------------------------------------------------------------------------
| Get Unmapped RFIDs
|--------------------------------------------------------------------------
| GET /api/v1/rfid/unmapped
|--------------------------------------------------------------------------
*/
router.get(
  "/unmapped",
  getUnmappedRFIDs
);





/*
|--------------------------------------------------------------------------
| Map RFID To Citizen
|--------------------------------------------------------------------------
| PATCH /api/v1/rfid/map
|--------------------------------------------------------------------------
*/
router.patch(
  "/map",
  mapRFID
);





/*
|--------------------------------------------------------------------------
| Get RFID Details By RFID Number
|--------------------------------------------------------------------------
| GET /api/v1/rfid/rfid/:rfid
|--------------------------------------------------------------------------
*/
router.get(
  "/rfid/:rfid",
  getRFIDByValue
);





/*
|--------------------------------------------------------------------------
| Get Citizen RFIDs By Phone Number
|--------------------------------------------------------------------------
| GET /api/v1/rfid/citizen/:phoneNumber
|--------------------------------------------------------------------------
*/
router.get(
  "/citizen/:phoneNumber",
  getCitizenRFIDs
);





module.exports = router;