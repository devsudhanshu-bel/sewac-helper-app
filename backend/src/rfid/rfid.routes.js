const express =
  require("express");

const router =
  express.Router();

const {

  createRFID,

  mapRFID,

  getAllRFIDMappings,

  getUnmappedRFIDs,

  getRFIDByValue,

  getCitizenRFIDs,

} = require("./rfid.controller");





/*
|--------------------------------------------------------------------------
| Create RFID
|--------------------------------------------------------------------------
| POST /api/v1/rfid/create/:rfid
|--------------------------------------------------------------------------
| Scanner sends raw RFID UID
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/create/E2000016351702081890ABCD
|--------------------------------------------------------------------------
| Backend Automatically Generates:
| SLNO -> 00001521
|--------------------------------------------------------------------------
*/
router.post(

  "/create/:rfid",

  createRFID

);





/*
|--------------------------------------------------------------------------
| OPTIONAL TEST ROUTE
|--------------------------------------------------------------------------
| Allows browser testing
|--------------------------------------------------------------------------
*/
router.get(

  "/create/:rfid",

  createRFID

);





/*
|--------------------------------------------------------------------------
| Get All RFID Mappings
|--------------------------------------------------------------------------
| GET /api/v1/rfid/all
|--------------------------------------------------------------------------
*/
router.get(

  "/all",

  getAllRFIDMappings

);





/*
|--------------------------------------------------------------------------
| Get Unmapped RFIDs
|--------------------------------------------------------------------------
| GET /api/v1/rfid/unmapped
|--------------------------------------------------------------------------
*/
router.get(

  "/unmapped",

  getUnmappedRFIDs

);





/*
|--------------------------------------------------------------------------
| Map RFID To Citizen
|--------------------------------------------------------------------------
| PATCH /api/v1/rfid/map
|--------------------------------------------------------------------------
| BODY:
| {
|   "slno": "00001521",
|   "phoneNumber": "9876543210",
|   "wasteType": "DRY"
| }
|--------------------------------------------------------------------------
*/
router.patch(

  "/map",

  mapRFID

);





/*
|--------------------------------------------------------------------------
| Get RFID Details By SLNO
|--------------------------------------------------------------------------
| GET /api/v1/rfid/rfid/:slno
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/rfid/00001521
|--------------------------------------------------------------------------
*/
router.get(

  "/rfid/:slno",

  getRFIDByValue

);





/*
|--------------------------------------------------------------------------
| Get Citizen RFIDs
|--------------------------------------------------------------------------
| GET /api/v1/rfid/citizen/:phoneNumber
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/citizen/9876543210
|--------------------------------------------------------------------------
*/
router.get(

  "/citizen/:phoneNumber",

  getCitizenRFIDs

);





/*
|--------------------------------------------------------------------------
| Health Check
|--------------------------------------------------------------------------
| GET /api/v1/rfid/health
|--------------------------------------------------------------------------
| Useful for:
| - Render
| - Railway
| - UptimeRobot
|--------------------------------------------------------------------------
*/
router.get(

  "/health",

  (req, res) => {

    return res.status(200).json({

      success: true,

      message:
        "RFID service is running",

      timestamp:
        new Date(),

    });

  }

);





module.exports = router;