const express =
  require("express");

const router =
  express.Router();

const {

  createRFID,

  mapRFID,

  getAllRFIDMappings,

  getUnmappedRFIDs,

  getRFIDByValue,

  getCitizenRFIDs,

} = require("./rfid.controller");





/*
|--------------------------------------------------------------------------
| HEALTH CHECK
|--------------------------------------------------------------------------
| GET /api/v1/rfid/health
|--------------------------------------------------------------------------
| Useful For:
| - Render
| - Railway
| - UptimeRobot
|--------------------------------------------------------------------------
*/
router.get(

  "/health",

  (req, res) => {

    return res.status(200).json({

      success: true,

      message:
        "RFID service is running",

      timestamp:
        new Date(),

    });

  }

);





/*
|--------------------------------------------------------------------------
| CREATE RFID
|--------------------------------------------------------------------------
| POST /api/v1/rfid/create/:rfid
|--------------------------------------------------------------------------
| Scanner Sends Raw RFID UID
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/create/E2000016351702081890ABCD
|--------------------------------------------------------------------------
| Backend Automatically Generates:
| SLNO -> 00001521
|--------------------------------------------------------------------------
*/
router.post(

  "/create/:rfid",

  createRFID

);





/*
|--------------------------------------------------------------------------
| OPTIONAL TEST ROUTE
|--------------------------------------------------------------------------
| Allows Browser Testing
|--------------------------------------------------------------------------
*/
router.get(

  "/create/:rfid",

  createRFID

);





/*
|--------------------------------------------------------------------------
| GET ALL RFID MAPPINGS
|--------------------------------------------------------------------------
| GET /api/v1/rfid/all
|--------------------------------------------------------------------------
*/
router.get(

  "/all",

  getAllRFIDMappings

);





/*
|--------------------------------------------------------------------------
| GET UNMAPPED RFIDS
|--------------------------------------------------------------------------
| GET /api/v1/rfid/unmapped
|--------------------------------------------------------------------------
| Returns RFIDs That:
| - Have No Phone Number
|--------------------------------------------------------------------------
*/
router.get(

  "/unmapped",

  getUnmappedRFIDs

);





/*
|--------------------------------------------------------------------------
| MAP RFID TO CITIZEN
|--------------------------------------------------------------------------
| PATCH /api/v1/rfid/map
|--------------------------------------------------------------------------
|
| SUPPORTS:
|
| CASE 1:
| Only DRY mapped
|
| CASE 2:
| Only WET mapped
|
| CASE 3:
| Both mapped
|
| CASE 4:
| Later second RFID mapped
|
|--------------------------------------------------------------------------
| BODY:
|
| {
|   "slno": "00001521",
|   "phoneNumber": "9876543210",
|   "wasteType": "DRY"
| }
|--------------------------------------------------------------------------
*/
router.patch(

  "/map",

  mapRFID

);





/*
|--------------------------------------------------------------------------
| GET RFID DETAILS BY SLNO
|--------------------------------------------------------------------------
| GET /api/v1/rfid/rfid/:slno
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/rfid/00001521
|--------------------------------------------------------------------------
*/
router.get(

  "/rfid/:slno",

  getRFIDByValue

);





/*
|--------------------------------------------------------------------------
| GET CITIZEN RFIDS
|--------------------------------------------------------------------------
| GET /api/v1/rfid/citizen/:phoneNumber
|--------------------------------------------------------------------------
| Example:
| /api/v1/rfid/citizen/9876543210
|--------------------------------------------------------------------------
|
| RESPONSE:
|
| {
|   "phoneNumber": "9876543210",
|
|   "dry": {
|      "slno": "00000001",
|      "rfid": "E200001"
|   },
|
|   "wet": null,
|
|   "isFullyMapped": false
| }
|--------------------------------------------------------------------------
*/
router.get(

  "/citizen/:phoneNumber",

  getCitizenRFIDs

);





module.exports = router;