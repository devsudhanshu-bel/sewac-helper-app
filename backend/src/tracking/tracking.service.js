const { prisma } =
  require("../config/db");





/*
|--------------------------------------------------------------------------
| Create Tracking Log
|--------------------------------------------------------------------------
*/
const createTrackingLogService = async ({

  /*
  |--------------------------------------------------------------------------
  | WORKER
  |--------------------------------------------------------------------------
  */
  workerId,



  /*
  |--------------------------------------------------------------------------
  | CITIZEN
  |--------------------------------------------------------------------------
  */
  slno,

  phoneNumber,

  citizenName,



  /*
  |--------------------------------------------------------------------------
  | RFID SNAPSHOT
  |--------------------------------------------------------------------------
  */
  drySlno,

  wetSlno,



  /*
  |--------------------------------------------------------------------------
  | NOT FOUND DETAILS
  |--------------------------------------------------------------------------
  */
  address,

  buildingNo,

  floorNo,

  photoUrl,



  /*
  |--------------------------------------------------------------------------
  | GEO LOCATION
  |--------------------------------------------------------------------------
  */
  latitude,

  longitude,



  /*
  |--------------------------------------------------------------------------
  | STATUS
  |--------------------------------------------------------------------------
  */
  status,



  /*
  |--------------------------------------------------------------------------
  | REMARKS
  |--------------------------------------------------------------------------
  */
  remarks,

}) => {

  /*
  |--------------------------------------------------------------------------
  | CREATE TRACKING LOG
  |--------------------------------------------------------------------------
  */
  const trackingLog =
    await prisma.trackingLog.create({

      data: {

        /*
        |--------------------------------------------------------------------------
        | WORKER
        |--------------------------------------------------------------------------
        */
        workerId,



        /*
        |--------------------------------------------------------------------------
        | CITIZEN
        |--------------------------------------------------------------------------
        */
        slno:
          slno || null,



        phoneNumber:
          phoneNumber || null,



        citizenName:
          citizenName || null,



        /*
        |--------------------------------------------------------------------------
        | RFID SNAPSHOT
        |--------------------------------------------------------------------------
        */
        drySlno:
          drySlno || null,



        wetSlno:
          wetSlno || null,



        /*
        |--------------------------------------------------------------------------
        | NOT FOUND DETAILS
        |--------------------------------------------------------------------------
        */
        address:
          address || null,



        buildingNo:
          buildingNo || null,



        floorNo:
          floorNo || null,



        photoUrl:
          photoUrl || null,



        /*
        |--------------------------------------------------------------------------
        | GEO LOCATION
        |--------------------------------------------------------------------------
        */
        latitude:
          latitude !== undefined
            ? parseFloat(latitude)
            : null,



        longitude:
          longitude !== undefined
            ? parseFloat(longitude)
            : null,



        /*
        |--------------------------------------------------------------------------
        | STATUS
        |--------------------------------------------------------------------------
        */
        status,



        /*
        |--------------------------------------------------------------------------
        | REMARKS
        |--------------------------------------------------------------------------
        */
        remarks:
          remarks || null,

      },

    });



  return trackingLog;
};





/*
|--------------------------------------------------------------------------
| Get All Tracking Logs
|--------------------------------------------------------------------------
*/
const getAllTrackingLogsService =
  async () => {

    const logs =
      await prisma.trackingLog.findMany({

        orderBy: {
          createdAt: "desc",
        },

      });



    return logs;
  };





/*
|--------------------------------------------------------------------------
| Get Tracking Logs By Worker
|--------------------------------------------------------------------------
*/
const getTrackingLogsByWorkerService =
  async (workerId) => {

    const logs =
      await prisma.trackingLog.findMany({

        where: {
          workerId,
        },

        orderBy: {
          createdAt: "desc",
        },

      });



    return logs;
  };





/*
|--------------------------------------------------------------------------
| Get Tracking Logs By Status
|--------------------------------------------------------------------------
*/
const getTrackingLogsByStatusService =
  async (status) => {

    const logs =
      await prisma.trackingLog.findMany({

        where: {
          status,
        },

        orderBy: {
          createdAt: "desc",
        },

      });



    return logs;
  };





module.exports = {

  createTrackingLogService,

  getAllTrackingLogsService,

  getTrackingLogsByWorkerService,

  getTrackingLogsByStatusService,

};